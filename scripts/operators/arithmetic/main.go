package main

import "fmt"

func main() {
	a := 30
	b := 50
	fmt.Println("A + B = ", a+b)
	fmt.Println("A - B = ", a-b)
	fmt.Println("A * B = ", a*b)
	fmt.Println("A / B = ", a/b)
	fmt.Println("A % B = ", a%b)

	k := 3
	j := 20
	fmt.Println("k = ", k)
	fmt.Println("j = ", j)
	k++
	j--
	fmt.Println("k = ", k)
	fmt.Println("j = ", j)
}
